package com.sdl.webapp.common.api;

public class LocalizationConfigurationLoaderException extends Exception {

    public LocalizationConfigurationLoaderException() {
    }

    public LocalizationConfigurationLoaderException(String message) {
        super(message);
    }

    public LocalizationConfigurationLoaderException(String message, Throwable cause) {
        super(message, cause);
    }

    public LocalizationConfigurationLoaderException(Throwable cause) {
        super(cause);
    }

    public LocalizationConfigurationLoaderException(String message, Throwable cause, boolean enableSuppression, boolean writableStackTrace) {
        super(message, cause, enableSuppression, writableStackTrace);
    }
}
